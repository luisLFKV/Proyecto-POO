    private void actualizarInterfaz() {
        String tipoProducto = tipo[cboTipo.getSelectedIndex()];
        if (tipoProducto == "Electronico") {
            jLabelAux.setText("Modelo");
            txtAux.setText("");
        } else if (tipoProducto == "Alimento") {
            jLabelAux.setText("Fecha de Caducidad");
            txtAux.setText("");
        }
    }

    private void replaceValors(){
        this.producto = Aplicacion.listaProductos.get(index);
        if (this.producto instanceof ProductoAlimento) {
            ProductoAlimento productoAlimento = (ProductoAlimento)this.producto;
            this.cboTipo.setSelectedIndex(1);
            this.jLabelAux.setText("Fecha de Caducidad");
            this.txtAux.setText(productoAlimento.getFechaCaducidad());
        } else if (this.producto instanceof ProductoElectronico) {
            ProductoElectronico productoElectronico = (ProductoElectronico)this.producto;
            this.cboTipo.setSelectedIndex(0);
            this.jLabelAux.setText("Modelo");
            this.txtAux.setText(productoElectronico.getModelo());
        }
        this.txtDesc.setText(producto.getDescripcion());
        this.txtNombreP.setText(producto.getNombre());
        this.txtCantidad.setText(producto.getStock()+"");
        this.txtId.setText(producto.getCodigo());
        this.txtPrecio.setText(producto.getPrecio()+"");
        this.txtPovDireccion.setText(producto.getProveedor().getDireccion());
        this.txtProvContacto.setText(producto.getProveedor().getContacto());
        this.txtProvName.setText(producto.getProveedor().getNombre());
    }

