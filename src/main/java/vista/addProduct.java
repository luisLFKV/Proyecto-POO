    private void jButtonSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSaveMouseClicked
        // TODO add your handling code here:
        String name = this.txtNombreP.getText();
        String desc = this.txtDesc.getText();
        double precio = Double.parseDouble(this.txtPrecio.getText());
        int cant = Integer.parseInt(this.txtCantidad.getText());
        String id = this.txtId.getText();
        String aux = this.txtAux.getText();

        String tipoProducto = tipo[cboTipo.getSelectedIndex()];
        
        if (this.opc == 1) {
            if (tipoProducto == "Electronico") {
                Aplicacion.listaProductos.add(new ProductoElectronico(aux, name, desc, precio, cant, id, new Proveedor(this.txtProvName.getText(), this.txtPovDireccion.getText(), this.txtProvContacto.getText())));
            } else if (tipoProducto == "Alimento") {
                Aplicacion.listaProductos.add(new ProductoAlimento(aux, name, desc, precio, cant, id, new Proveedor(this.txtProvName.getText(), this.txtPovDireccion.getText(), this.txtProvContacto.getText())));
            }
            //Se limpian los campos
            this.txtAux.setText("");
            this.txtDesc.setText("");
            this.txtNombreP.setText("");
            this.txtCantidad.setText("");
            this.txtId.setText("");
            this.txtPrecio.setText("");
            this.txtPovDireccion.setText("");
            this.txtProvContacto.setText("");
            this.txtProvName.setText("");
        } else {
            if (tipoProducto == "Electronico") {
                Aplicacion.listaProductos.set(index, new ProductoElectronico(aux, name, desc, precio, cant, id, new Proveedor(this.txtProvName.getText(), this.txtPovDireccion.getText(), this.txtProvContacto.getText())));
            } else if (tipoProducto == "Alimento") {
                Aplicacion.listaProductos.set(index, new ProductoAlimento(aux, name, desc, precio, cant, id, new Proveedor(this.txtProvName.getText(), this.txtPovDireccion.getText(), this.txtProvContacto.getText())));
            }
        }

        RWProductos.escribirDatos(Aplicacion.listaProductos);
        JOptionPane.showMessageDialog(this, "Datos guardados");
    }//GEN-LAST:event_jButtonSaveMouseClicked

